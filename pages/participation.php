<p>
We very much welcome practical papers or software demos that outline 
artistic creative processes or workflows based on Linux audio 
applications or systems ("how I use Linux Audio applications to create 
my music/media art").
</p>
<p>
Not all papers need to directly address Linux Audio. There is no 
limitation for subjects from related fields like audio/visual research 
and open content.
</p>

<p>NB. Papers should be as complete as possible on submission. Until 
submission closes it is possible to replace/update files in the <a href="http://lac.linuxaudio.org/2015/PaperSubmission/">openconf</a>
 system. Submissions will be peer-reviewed and after the notification of
 acceptance there is a time window for updates and last-minute changes.
</p>

<h1>Call for Music, Installations and Workshops</h1>
<p>
Music, Installations and Workshops can be submitted for review via the
<strong><a href="http://lac.linuxaudio.org/2015/MusicSoundArtMisc/">online music/installations/workshops submission system</a></strong>. We are looking for music and sound art that has been produced or 
composed entirely or mostly using GNU/Linux and other open source music software for:
</p>
<ul>
  <li>The Electroacoustic Music Concerts (fixed media or live performance works)</li>
  <li>The Linux Sound Night (danceable, live performances preferred)</li>
  <li>Audio/Visual installations</li>
</ul>
<p>Proposals for 60-120 min. <strong>workshops</strong> should be submitted through the same system. Eligible topics are the same as for the main track of the conference (see above), but may be presented in various forms, including (but not limited to) tutorials and presentations of hardware and software systems, open discussions, developer meetings, live coding sessions, etc.</p>
<p>Please download the complete <strong><a href="download/lac2015-call-for-miw.pdf">Call for Music, Installations and Workshops</a></strong> document (PDF) before trying to submit. This contains all the details about the submission and review process, submission types and venues.</p>
<p>The electroacoustic concerts will take place at the main concert hall ("Roter Saal") of the Mainz School of Music (Hochschule für Musik) which has 220 seats; 8 channel audio equipment will be provided. The Linux Sound Night will be at the "Baron" where a stage, stereo PA equipment, lighting set and projector system will be available. For the sound installations we offer the "Faculty Room" (96 sq. meters) which is located in close vicinity to the lecture halls where the paper sessions and workshops are. All venues are on the campus of the JGU, within walking distance.</p>
