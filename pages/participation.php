<h1>Call for Papers, Posters and Presentations</h1>
<p>
<strong>Papers</strong> can be submitted for review via the online
<strong><a href="http://lac.linuxaudio.org/2015/PaperSubmission/">paper submission system</a></strong>.
Please use the available <strong><a href="http://lac.linuxaudio.org/2015/download/lac2015-templates.tar.gz">templates for papers</a></strong>. The tarball includes templates for LaTeX and OpenOffice/LibreOffice including information about margins and layout.
</p>

<p>
Papers must be written and presented in English, 4—8 pages, with abstract (50—250 words) and up to 5 keywords.
In general a presentation should take about half an hour.
</p>

<p>
The copyright of the paper remains with the author, but we reserve the 
right to create and sell printed proceedings from all submitted (and 
accepted) papers.
</p>

<p>
For <strong>poster presentations</strong> or <strong>lightning talks</strong>
 we only require an abstract (50—250 words).
You may optionally provide written documentation for inclusion in the 
printed proceedings (if you choose to do so, please use one of the 
templates, minimum two pages). The duration of a lightning talk will be 
about 5 minutes, posters will be presented in a separate poster session.
</p>

<p>
Please highlight the presentation format (Topic: «poster», 
«lightning-talk» in the submission system).
With exception of submission length and presentation duration, the same 
rules as for papers also apply to poster presentations and 
lightning talks.
</p>

