<h3>By Car</h3>
<p>
Please check the attached <a href="http://www.uni-mainz.de/Dateien/JGU_directions.pdf" rel="external">map</a> (pdf) for driving directions.
</p>
<p>
<strong>From the west (i.e. from direction Bonn/Cologne)
<br /></strong>
• Travel autobahn A 60 crossing &quot;Autobahn-Dreieck Mainz&quot; direction Darmstadt.
<br />
• Leave autobahn A 60 at exit &quot;Mainz-Finthen&quot;.
<br />
• Follow the street sign &quot;Saarstraße/Innenstadt&quot; straight ahead through the traffic circle &quot;Europaplatz&quot;.
<br />
• Take exit &quot;Universität&quot;.
</p>
<p>
<strong>From the east (i.e. from direction Würzburg/Darmstadt)
<br /></strong> • Travel autobahn A 60 crossing &quot;Autobahn-Dreieck Rüsselsheim&quot; OR travel autobahn A 63 crossing &quot;Autobahnkreuz Mainz&quot; to get to autobahn A 60 direction Bingen.
<br />
• Leave autobahn A 60 at exit &quot;Mainz-Finthen&quot;.
<br />
• Follow the street sign &quot;Saarstraße/Innenstadt&quot; straight ahead through the traffic circle &quot;Europaplatz&quot;.
<br />
• Take exit &quot;Universität&quot;.
</p>
<p>
<strong>From the north (i.e. from direction Kassel)
<br /></strong> • Follow autobahn A 66.
<br />
• At &quot;Schiersteiner Kreuz&quot; change to autobahn A 643 direction Mainz.
<br />
• Follow autobahn A 643 to &quot;Autobahn-Dreieck Mainz&quot; and change here to autobahn A 60 direction Darmstadt.
<br />
• Leave autobahn A 60 at exit &quot;Mainz-Finthen&quot;.
<br />
• Follow the street sign &quot;Saarstraße/Innenstadt&quot; straight ahead through the traffic circle &quot;Europaplatz&quot;.
<br />
• Take exit &quot;Universität&quot;.
</p>
<p>
<strong>Parking</strong> on campus is available only with special permission. Please see the <a href="http://www.uni-mainz.de/Dateien/JGU_campus_map_w-out_lecture_halls.pdf" rel="external">site map</a> (pdf) for parking in the nearest vicinity of the campus. Parking is also available at the near-by football stadium, which is about 200 meters from the University’s main entrance.
</p>

