<p>
Will you still be around on Sunday? At <strong>10 a.m.</strong> we will take off at the "Muschel" (the shell-like lecture building, Johann-Joachim-Becher-Weg 23 on the campus, not far from the conference venue) for an excursion to <span xml:lang="de" lang="de"><a href="http://www.ruedesheim.de/en/" rel="external"><strong>Rüdesheim am Rhein</strong></a></span> in the lovely upper middle Rhine valley (Unesco World Heritage).
</p>

<p>This will include a guided tour (in English) of <span xml:lang="de" lang="de"><a href="http://www.siegfrieds-musikkabinett.de/frame.htm" rel="external">Siegfried's Mechanisches Musikkabinett</a></span> (museum of mechanical music automata) and a winetasting at the <span xml:lang="de" lang="de"><a href="http://www.weingut-adolf-stoerzel.de/Dt_Weinproben.php" rel="external">Weingut Adolf Störzel</a></span>. Afterwards there will be opportunity to stroll around the town and have lunch in small groups in the restaurants there. The bus will take off again at <strong>3 p.m.</strong> and we should be back at Mainz around <strong>4 p.m.</strong>.
</p>

<p>You decide for yourself whether you want to join us at the museum and/or the winetasting, but we kindly ask you to indicate this in your reservation email (see below), so that we can inform the museum and the vineyard about the number of people in the group. Prices per person are:</p>

<ul>
<li><strong>8 €</strong> (Euros) for the bus.</li>
<li><strong>7 €</strong> for the museum (<strong>4 €</strong> for students, please make sure that you have your student card with you). Depending on how many people will join us there, we may still be able to negotiate reduced group prices.</li>
<li><strong>9 €</strong> for the winetasting (4 glasses of different Rheingau wines, incl. pretzels).</li>
</ul>
<p style="clear:left;"></p>

<p>We have chartered a bus with 50 seats, these will be allocated on a first come first served basis, so please make sure that you <strong>reserve your seat</strong> asap! You can do so by mailing lac@linuxaudio.org with the following information:</p>

<ul>
<li><strong class = "red">Please always use the following line as the subject of your email:</strong> <strong>LAC 2015 excursion</strong></li>
<li>Your name and email address.</li>
<li>Please indicate whether you'll join us at the museum.</li>
<li>Please indicate whether you'll join us at the winetasting.</li>
<li>Please indicate the number of persons (if you'll be going with, e.g., your spouse and kids). Otherwise a default value of 1 is assumed. ;-)</li>
</ul>
<p style="clear:left;"></p>

<p>You'll get an email confirmation of your reservation (the requests will be processed manually, so please give it 1-2 days before inquiring).</p>

<p>Please note that this is for <strong>registered participants</strong> only. The bus fee will be collected when you show up at the registration desk to get your badge and information leaflets.</p>

<p>Alternatively, you can also reserve your seat directly at the registration desk, provided there are still any available.</p>
