<h1>About</h1>
<p>
The Linux Audio Conference (LAC) is the international conference about
Free/Open-Source Software for music, sound and other media with GNU/Linux as the main platform.
The 2015 edition (the 13th installment of the conference) will take place at the <em>Johannes Gutenberg University</em> (JGU) Mainz, Germany.
</p>

<h1>Target audience</h1>
<p>
The Linux Audio Conference brings together musicians, composers, sound 
artists, software developers, researchers and engineers working with 
GNU/Linux as an open, stable, professional platform for audio and media 
research and music production.
</p>

