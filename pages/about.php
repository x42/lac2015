<p>
  With about 36,500 students from about 130 nations, Johannes Gutenberg University Mainz (JGU) is one of the ten largest universities in Germany. As the only comprehensive university in Rhineland-Palatinate, JGU combines almost all academic disciplines under one roof, including the Mainz University Medical Center, the School of Music, and the Mainz Academy of Arts, which is a unique feature in the German academic landscape.
</p>
<p>
  The JGU is also one of the oldest universities in Germany. Founded in 1477 during the era of Johannes Gutenberg and reopened after a 150-year break in 1946 by the French forces then based in Germany, Johannes Gutenberg University owes much to the man whose name it bears and his achievements. In keeping with the Gutenberg Spirit, the JGU has set itself the mission of "moving minds and crossing boundaries." Interdisciplinary discourse is therefore a hallmark of our university.
</p>
<p>
  This is also true for the <a target="_blank" href="http://www.musikwissenschaft.uni-mainz.de/Musikinformatik/" rel="external">Computer Music Research Group</a> (Bereich Musikinformatik) of the JGU which will host LAC 2015. Being founded in 1991, our research group has been among the first German academic institutions in this interdisciplinary field at the intersection of music, mathematics, computer science and media technology. In our media lab students are working almost exclusively with Linux, and in our research we are also devoted to contributing to the growing body of open source audio and computer music software.
</p>

<h1>Who?</h1>
<p>LAC is organized by a community of GNU/Linux free/libre software users and developers that communicate through the <a href="http://lists.linuxaudio.org/">Linux-Audio-Users and Linux-Audio-Developers</a> mailing lists.
LAC 2015 is under the auspices of the the <a target="_blank" href="http://www.ikm.uni-mainz.de/" rel="external">IKM</a> (Institut für Kunstgeschichte und Musikwissenschaft) of the JGU, and is organized by <a target="_blank" href="http://www.musikwissenschaft.uni-mainz.de/Musikinformatik/e_ag.php" rel="external">Albert Gräf</a> and his team at the <a target="_blank" href="http://www.musikwissenschaft.uni-mainz.de/Musikinformatik/" rel="external">Computer Music Research Group</a> (Bereich Musikinformatik) of the IKM.</p>


